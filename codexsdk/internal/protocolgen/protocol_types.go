func quoted(value string) string {
	raw, _ := json.Marshal(value)
	return string(raw)
}

func writeStructType(out *bytes.Buffer, typ TypePlan) {
	if len(typ.Fields) == 0 {
		fmt.Fprintf(out, "type %s struct{}\n", typ.TypeName)
		fmt.Fprintf(out, "\nfunc (value *%s) UnmarshalJSON(data []byte) error {\n", typ.TypeName)
		fmt.Fprintf(out, "\tfields, err := decodeObjectFields(data, %q)\n", typ.TypeName)
		out.WriteString("\tif err != nil {\n")
		out.WriteString("\t\treturn err\n")
		out.WriteString("\t}\n")
		fmt.Fprintf(out, "\tif err := rejectUnexpectedFields(fields, %q); err != nil {\n", typ.TypeName)
		out.WriteString("\t\treturn err\n")
		out.WriteString("\t}\n")
		fmt.Fprintf(out, "\t*value = %s{}\n", typ.TypeName)
		out.WriteString("\treturn nil\n")
		out.WriteString("}\n")
		return
	}
	fmt.Fprintf(out, "type %s struct {\n", typ.TypeName)
	fields := append([]FieldPlan(nil), typ.Fields...)
	sort.Slice(fields, func(i, j int) bool {
		return fields[i].FieldName < fields[j].FieldName
	})
	for _, field := range fields {
		fmt.Fprintf(out, "\t%s %s `json:\"%s%s\"`\n",
			fieldGoName(field.FieldName),
			generatedGoType(field.GoType),
			field.FieldName,
			jsonTagOmitEmpty(field),
		)
	}
	if typ.OpenDynamicProperties {
		out.WriteString("\tDynamicProperties map[string]JSONValue\n")
	}
	out.WriteString("}\n")
	writeStructMarshal(out, typ, fields)
	writeStructDecoder(out, typ, fields)
}

func writeStructMarshal(out *bytes.Buffer, typ TypePlan, fields []FieldPlan) {
	var guardedFields []FieldPlan
	var minItemsFields []FieldPlan
	var minimumFields []FieldPlan
	for _, field := range fields {
		if fieldNeedsMarshalNilCheck(field) {
			guardedFields = append(guardedFields, field)
		}
		if field.MinItems != nil {
			minItemsFields = append(minItemsFields, field)
		}
		if field.Minimum != nil {
			minimumFields = append(minimumFields, field)
		}
	}
	if len(guardedFields) == 0 && len(minItemsFields) == 0 && len(minimumFields) == 0 && !typ.OpenDynamicProperties {
		return
	}
	out.WriteString("\n")
	fmt.Fprintf(out, "func (value %s) MarshalJSON() ([]byte, error) {\n", typ.TypeName)
	for _, field := range guardedFields {
		fmt.Fprintf(out, "\tif value.%s == nil {\n", fieldGoName(field.FieldName))
		fmt.Fprintf(out, "\t\treturn nil, fmt.Errorf(\"encode %s.%s: nil is not allowed\")\n", typ.TypeName, field.FieldName)
		out.WriteString("\t}\n")
	}
	for _, field := range minItemsFields {
		condition := minItemsViolationCondition("value."+fieldGoName(field.FieldName), field.GoType, minItemsLiteral(field))
		fmt.Fprintf(out, "\tif %s {\n", condition)
		fmt.Fprintf(out, "\t\treturn nil, fmt.Errorf(\"encode %s.%s: must contain at least %s item\")\n", typ.TypeName, field.FieldName, minItemsLiteral(field))
		out.WriteString("\t}\n")
	}
	for _, field := range minimumFields {
		condition := minimumViolationCondition("value."+fieldGoName(field.FieldName), field.GoType, minimumLiteral(field))
		fmt.Fprintf(out, "\tif %s {\n", condition)
		fmt.Fprintf(out, "\t\treturn nil, fmt.Errorf(\"encode %s.%s: value must be >= %s\")\n", typ.TypeName, field.FieldName, minimumLiteral(field))
		out.WriteString("\t}\n")
	}
	if !typ.OpenDynamicProperties {
		fmt.Fprintf(out, "\ttype wire %s\n", typ.TypeName)
		out.WriteString("\treturn json.Marshal(wire(value))\n")
		out.WriteString("}\n")
		return
	}
	out.WriteString("\ttype wire struct {\n")
	for _, field := range fields {
		fmt.Fprintf(out, "\t\t%s %s `json:\"%s%s\"`\n",
			fieldGoName(field.FieldName),
			generatedGoType(field.GoType),
			field.FieldName,
			jsonTagOmitEmpty(field),
		)
	}
	out.WriteString("\t}\n")
	out.WriteString("\tdata, err := json.Marshal(wire{\n")
	for _, field := range fields {
		fieldName := fieldGoName(field.FieldName)
		fmt.Fprintf(out, "\t\t%s: value.%s,\n", fieldName, fieldName)
	}
	out.WriteString("\t})\n")
	out.WriteString("\tif err != nil {\n")
	fmt.Fprintf(out, "\t\treturn nil, fmt.Errorf(\"encode %s: %%w\", err)\n", typ.TypeName)
	out.WriteString("\t}\n")
	out.WriteString("\tencoded, err := ParseJSONValue(data)\n")
	out.WriteString("\tif err != nil {\n")
	fmt.Fprintf(out, "\t\treturn nil, fmt.Errorf(\"encode %s: %%w\", err)\n", typ.TypeName)
	out.WriteString("\t}\n")
	out.WriteString("\tobject, ok := encoded.AsObject()\n")
	out.WriteString("\tif !ok {\n")
	fmt.Fprintf(out, "\t\treturn nil, fmt.Errorf(\"encode %s: expected object\")\n", typ.TypeName)
	out.WriteString("\t}\n")
	out.WriteString("\tfor key, dynamicValue := range value.DynamicProperties {\n")
	out.WriteString("\t\tswitch key {\n")
	for _, field := range fields {
		fmt.Fprintf(out, "\t\tcase %q:\n", field.FieldName)
		fmt.Fprintf(out, "\t\t\treturn nil, fmt.Errorf(\"encode %s.DynamicProperties[%%q]: conflicts with declared field\", key)\n", typ.TypeName)
	}
	out.WriteString("\t\t}\n")
	out.WriteString("\t\tif !dynamicValue.IsValid() {\n")
	fmt.Fprintf(out, "\t\t\treturn nil, fmt.Errorf(\"encode %s.DynamicProperties[%%q]: invalid JSONValue\", key)\n", typ.TypeName)
	out.WriteString("\t\t}\n")
	out.WriteString("\t\tif _, err := dynamicValue.MarshalJSON(); err != nil {\n")
	fmt.Fprintf(out, "\t\t\treturn nil, fmt.Errorf(\"encode %s.DynamicProperties[%%q]: %%w\", key, err)\n", typ.TypeName)
	out.WriteString("\t\t}\n")
	out.WriteString("\t\tobject[key] = dynamicValue.clone()\n")
	out.WriteString("\t}\n")
	out.WriteString("\treturn JSONObject(object).MarshalJSON()\n")
	out.WriteString("}\n")
}

func fieldNeedsMarshalNilCheck(field FieldPlan) bool {
	if !field.Required || field.WireAllowsNull {
		return false
	}
	goType := generatedGoType(field.GoType)
	return strings.HasPrefix(goType, "[]") || strings.HasPrefix(goType, "map[string]")
}

func minItemsLiteral(field FieldPlan) string {
	if field.MinItems == nil {
		return "0"
	}
	return fmt.Sprintf("%d", *field.MinItems)
}

func minimumLiteral(field FieldPlan) string {
	if field.Minimum == nil {
		return "0"
	}
	return fmt.Sprintf("%.0f", *field.Minimum)
}

func minimumViolationCondition(target string, goType string, minimum string) string {
	generated := generatedGoType(goType)
	switch {
	case strings.HasPrefix(generated, "*Nullable["):
		return fmt.Sprintf("%s != nil && %s.Value != nil && *%s.Value < %s", target, target, target, minimum)
	case strings.HasPrefix(generated, "Nullable["):
		return fmt.Sprintf("%s.Value != nil && *%s.Value < %s", target, target, minimum)
	case strings.HasPrefix(generated, "*"):
		return fmt.Sprintf("%s != nil && *%s < %s", target, target, minimum)
	default:
		return fmt.Sprintf("%s < %s", target, minimum)
	}
}

func minItemsViolationCondition(target string, goType string, minItems string) string {
	generated := generatedGoType(goType)
	switch {
	case strings.HasPrefix(generated, "*Nullable["):
		return fmt.Sprintf("%s != nil && %s.Value != nil && len(*%s.Value) < %s", target, target, target, minItems)
	case strings.HasPrefix(generated, "Nullable["):
		return fmt.Sprintf("%s.Value != nil && len(*%s.Value) < %s", target, target, minItems)
	case strings.HasPrefix(generated, "*"):
		return fmt.Sprintf("%s != nil && len(*%s) < %s", target, target, minItems)
	default:
		return fmt.Sprintf("len(%s) < %s", target, minItems)
	}
}

func writeScalarUnionType(out *bytes.Buffer, union ScalarUnionPlan) {
	kindTypeName := union.TypeName + "Kind"
	fmt.Fprintf(out, "type %s string\n\n", kindTypeName)
	out.WriteString("const (\n")
	for _, variant := range union.Variants {
		fmt.Fprintf(out, "\t%s %s = %s\n", scalarUnionKindConstName(union, variant), kindTypeName, quoted(variant.DiscriminatorValue))
	}
	out.WriteString(")\n\n")

	fmt.Fprintf(out, "type %s struct {\n", union.TypeName)
	out.WriteString("\tkind " + kindTypeName + "\n")
	for _, variant := range union.Variants {
		fmt.Fprintf(out, "\t%s %s\n", variant.PrivateFieldName, variant.GoType)
	}
	out.WriteString("}\n\n")

	for _, variant := range union.Variants {
		fmt.Fprintf(out, "func %s(value %s) %s {\n", variant.ConstructorName, variant.GoType, union.TypeName)
		fmt.Fprintf(out, "\treturn %s{kind: %s, %s: value}\n", union.TypeName, scalarUnionKindConstName(union, variant), variant.PrivateFieldName)
		out.WriteString("}\n\n")
	}
	fmt.Fprintf(out, "func (value %s) Kind() %s {\n", union.TypeName, kindTypeName)
	out.WriteString("\treturn value.kind\n")
	out.WriteString("}\n\n")

	fmt.Fprintf(out, "func (value %s) IsValid() bool {\n", union.TypeName)
	out.WriteString("\tswitch value.kind {\n")
	for _, variant := range union.Variants {
		fmt.Fprintf(out, "\tcase %s:\n", scalarUnionKindConstName(union, variant))
		out.WriteString("\t\treturn true\n")
	}
	out.WriteString("\tdefault:\n")
	out.WriteString("\t\treturn false\n")
	out.WriteString("\t}\n")
	out.WriteString("}\n\n")

	for _, variant := range union.Variants {
		fmt.Fprintf(out, "func (value %s) %s() (%s, bool) {\n", union.TypeName, variant.AccessorName, variant.GoType)
		fmt.Fprintf(out, "\tif value.kind != %s {\n", scalarUnionKindConstName(union, variant))
		fmt.Fprintf(out, "\t\treturn %s, false\n", zeroValueLiteral(variant.GoType))
		out.WriteString("\t}\n")
		fmt.Fprintf(out, "\treturn value.%s, true\n", variant.PrivateFieldName)
		out.WriteString("}\n\n")
	}

	fmt.Fprintf(out, "func (value %s) MarshalJSON() ([]byte, error) {\n", union.TypeName)
	out.WriteString("\tswitch value.kind {\n")
	for _, variant := range union.Variants {
		fmt.Fprintf(out, "\tcase %s:\n", scalarUnionKindConstName(union, variant))
		if strings.HasPrefix(variant.GoType, "[]") {
			fmt.Fprintf(out, "\t\tif value.%s == nil {\n", variant.PrivateFieldName)
			fmt.Fprintf(out, "\t\t\treturn nil, fmt.Errorf(\"encode %s.%s: nil is not allowed\")\n", union.TypeName, variant.DiscriminatorValue)
			out.WriteString("\t\t}\n")
		}
		fmt.Fprintf(out, "\t\treturn json.Marshal(value.%s)\n", variant.PrivateFieldName)
	}
	out.WriteString("\tdefault:\n")
	fmt.Fprintf(out, "\t\treturn nil, invalidUnionValue(%q)\n", union.TypeName)
	out.WriteString("\t}\n")
	out.WriteString("}\n\n")

	fmt.Fprintf(out, "func (value *%s) UnmarshalJSON(data []byte) error {\n", union.TypeName)
	out.WriteString("\tdecoded, err := ParseJSONValue(data)\n")
	out.WriteString("\tif err != nil {\n")
	fmt.Fprintf(out, "\t\treturn fmt.Errorf(\"decode %s: %%w\", err)\n", union.TypeName)
	out.WriteString("\t}\n")
	out.WriteString("\tswitch decoded.Kind() {\n")
	for _, variant := range union.Variants {
		switch variant.JSONKind {
		case "string":
			out.WriteString("\tcase JSONKindString:\n")
			out.WriteString("\t\tparsed, _ := decoded.AsString()\n")
			fmt.Fprintf(out, "\t\t*value = %s(parsed)\n", variant.ConstructorName)
			out.WriteString("\t\treturn nil\n")
		case "number":
			out.WriteString("\tcase JSONKindNumber:\n")
			out.WriteString("\t\tnumber, _ := decoded.AsNumber()\n")
			out.WriteString("\t\tparsed, err := number.Int64()\n")
			out.WriteString("\t\tif err != nil {\n")
			fmt.Fprintf(out, "\t\t\treturn fmt.Errorf(\"decode %s: expected int64 JSON number: %%w\", err)\n", union.TypeName)
			out.WriteString("\t\t}\n")
			fmt.Fprintf(out, "\t\t*value = %s(parsed)\n", variant.ConstructorName)
			out.WriteString("\t\treturn nil\n")
		case "array":
			out.WriteString("\tcase JSONKindArray:\n")
			if variant.GoType == "[]string" {
				out.WriteString("\t\titems, _ := decoded.AsArray()\n")
				out.WriteString("\t\tparsed := make([]string, len(items))\n")
				out.WriteString("\t\tfor index, item := range items {\n")
				out.WriteString("\t\t\tparsedItem, ok := item.AsString()\n")
				out.WriteString("\t\t\tif !ok {\n")
				fmt.Fprintf(out, "\t\t\t\treturn fmt.Errorf(\"decode %s: expected array item %%d to be string\", index)\n", union.TypeName)
				out.WriteString("\t\t\t}\n")
				out.WriteString("\t\t\tparsed[index] = parsedItem\n")
				out.WriteString("\t\t}\n")
			} else {
				fmt.Fprintf(out, "\t\tvar parsed %s\n", variant.GoType)
				out.WriteString("\t\tif err := json.Unmarshal(data, &parsed); err != nil {\n")
				fmt.Fprintf(out, "\t\t\treturn fmt.Errorf(\"decode %s: expected array: %%w\", err)\n", union.TypeName)
				out.WriteString("\t\t}\n")
			}
			fmt.Fprintf(out, "\t\t*value = %s(parsed)\n", variant.ConstructorName)
			out.WriteString("\t\treturn nil\n")
		}
	}
	out.WriteString("\tdefault:\n")
	fmt.Fprintf(out, "\t\treturn fmt.Errorf(\"decode %s: expected %s\")\n", union.TypeName, scalarUnionExpectedKinds(union))
	out.WriteString("\t}\n")
	out.WriteString("}\n")
}

func scalarUnionKindConstName(union ScalarUnionPlan, variant ScalarUnionVariantPlan) string {
	return union.TypeName + "Kind" + variant.GoName
}

func scalarUnionExpectedKinds(union ScalarUnionPlan) string {
	var kinds []string
	for _, variant := range union.Variants {
		switch variant.JSONKind {
		case "number":
			kinds = append(kinds, "int64 JSON number")
		default:
			kinds = append(kinds, variant.JSONKind)
		}
	}
	sort.SliceStable(kinds, func(i, j int) bool {
		return scalarUnionExpectedKindRank(kinds[i]) < scalarUnionExpectedKindRank(kinds[j])
	})
	return strings.Join(kinds, " or ")
}

func scalarUnionExpectedKindRank(kind string) int {
	switch kind {
	case "string":
		return 0
	case "int64 JSON number":
		return 1
	case "array":
		return 2
	default:
		return 10
	}
}

func zeroValueLiteral(goType string) string {
	switch goType {
	case "int64":
		return "0"
	case "string":
		return `""`
	default:
		if strings.HasPrefix(goType, "[]") {
			return "nil"
		}
		return goType + "{}"
	}
}

