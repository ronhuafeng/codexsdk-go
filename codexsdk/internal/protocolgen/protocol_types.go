func taggedUnionCandidates(plan ProtocolTypePlan, resolver generatedDefinitionNameResolver) ([]TypePlan, error) {
	var candidates []TypePlan
	for _, typ := range plan.Types {
		if typ.Kind == TypePlanTaggedUnionCandidate && isGeneratedTaggedUnionCheckpoint(typ.SchemaPath) {
			candidates = append(candidates, typ)
		}
		definitions, err := generatedDefinitionTaggedUnionCandidates(typ, resolver)
		if err != nil {
			return nil, err
		}
		candidates = append(candidates, definitions...)
	}
	sort.Slice(candidates, func(i, j int) bool {
		return candidates[i].TypeName < candidates[j].TypeName
	})
	return candidates, nil
}

func generatedDefinitionTaggedUnionCandidates(parent TypePlan, resolver generatedDefinitionNameResolver) ([]TypePlan, error) {
	if parent.Schema == nil || len(parent.Schema.Definitions) == 0 {
		return nil, nil
	}
	var names []string
	for name, schema := range parent.Schema.Definitions {
		if isReviewedGeneratedDefinition(parent.SchemaPath, name) && classifyGeneratedDefinition(schema) == generatedDefinitionTaggedUnion {
			names = append(names, name)
		}
	}
	sort.Strings(names)

	var candidates []TypePlan
	for _, name := range names {
		schema := parent.Schema.Definitions[name]
		if schema == nil || len(schema.OneOf) == 0 {
			return nil, fmt.Errorf("definition tagged union %s in %s is not a oneOf schema", name, parent.SchemaPath)
		}
		candidates = append(candidates, TypePlan{
			Kind:       TypePlanTaggedUnionCandidate,
			Reason:     "oneOf schema definition selected for generated tagged union coverage",
			Schema:     schema,
			SchemaPath: parent.SchemaPath + "#/definitions/" + name,
			Stability:  parent.Stability,
			TypeName:   generatedDefinitionTypeName(resolver, parent.SchemaPath, name),
		})
	}
	return candidates, nil
}

func buildTaggedUnionPlan(typ TypePlan, generatedNamedTypes map[string]bool, resolver generatedDefinitionNameResolver) (TaggedUnionPlan, error) {
	if typ.Schema == nil || len(typ.Schema.OneOf) == 0 {
		return TaggedUnionPlan{}, fmt.Errorf("tagged union %s has no oneOf variants", typ.SchemaPath)
	}
	discriminator := ""
	valueSet := map[string]string{}
	nameSet := map[string]string{}
	union := TaggedUnionPlan{
		SchemaPath: typ.SchemaPath,
		TypeName:   typ.TypeName,
	}
	for index, variant := range typ.Schema.OneOf {
		if variant == nil || !variant.Type.Only("object") || len(variant.Properties) == 0 {
			return TaggedUnionPlan{}, fmt.Errorf("tagged union %s variant %d is not an object with properties", typ.SchemaPath, index)
		}
		variantDiscriminator, value, err := variantDiscriminator(variant)
		if err != nil {
			return TaggedUnionPlan{}, fmt.Errorf("tagged union %s variant %d: %w", typ.SchemaPath, index, err)
		}
		if discriminator == "" {
			discriminator = variantDiscriminator
			union.Discriminator = discriminator
		} else if discriminator != variantDiscriminator {
			return TaggedUnionPlan{}, fmt.Errorf("tagged union %s mixes discriminator fields %q and %q", typ.SchemaPath, discriminator, variantDiscriminator)
		}
		if previous, ok := valueSet[value]; ok {
			return TaggedUnionPlan{}, fmt.Errorf("tagged union %s discriminator value %q appears in both %s and %s", typ.SchemaPath, value, previous, variant.Title)
		}
		valueSet[value] = variant.Title

		goName := variantGoName(value)
		if previous, ok := nameSet[goName]; ok {
			return TaggedUnionPlan{}, fmt.Errorf("tagged union %s variants %q and %q both map to Go name %s", typ.SchemaPath, previous, value, goName)
		}
		nameSet[goName] = value

		fields, nullFields, err := taggedUnionVariantFields(typ, variant, discriminator, index, generatedNamedTypes, resolver)
		if err != nil {
			return TaggedUnionPlan{}, err
		}
		union.Variants = append(union.Variants, TaggedUnionVariantPlan{
			AccessorName:       "As" + goName,
			ConstructorName:    "New" + typ.TypeName + goName,
			DiscriminatorValue: value,
			Fields:             fields,
			GoName:             goName,
			NullFields:         nullFields,
			PayloadTypeName:    typ.TypeName + goName,
			PrivateFieldName:   unexportedIdentifier(goName),
		})
	}
	return union, nil
}

func variantDiscriminator(schema *Schema) (fieldName string, value string, err error) {
	required := schema.RequiredSet()
	var candidates []string
	for name, property := range schema.Properties {
		if !required[name] || property == nil || !property.Type.Only("string") || len(property.Enum) != 1 {
			continue
		}
		switch name {
		case "type", "method", "kind", "mode":
			candidates = append(candidates, name)
		}
	}
	sort.Strings(candidates)
	if len(candidates) != 1 {
		return "", "", fmt.Errorf("expected exactly one reviewed discriminator candidate, got %d", len(candidates))
	}
	fieldName = candidates[0]
	return fieldName, schema.Properties[fieldName].Enum[0], nil
}

func taggedUnionVariantFields(typ TypePlan, variant *Schema, discriminator string, variantIndex int, generatedNamedTypes map[string]bool, resolver generatedDefinitionNameResolver) ([]FieldPlan, []NullFieldPlan, error) {
	required := variant.RequiredSet()
	var fieldNames []string
	for name := range variant.Properties {
		if name == discriminator {
			continue
		}
		fieldNames = append(fieldNames, name)
	}
	sort.Strings(fieldNames)

	fields := make([]FieldPlan, 0, len(fieldNames))
	var nullFields []NullFieldPlan
	for _, name := range fieldNames {
		property := variant.Properties[name]
		if property.Type.Only("null") {
			if required[name] {
				return nil, nil, fmt.Errorf("tagged union %s variant %d field %s is a required null field", typ.SchemaPath, variantIndex, name)
			}
			nullFields = append(nullFields, NullFieldPlan{FieldName: name, Required: required[name]})
			continue
		}
		field, err := planField(CoverageField{
			Field:     name,
			Path:      fmt.Sprintf("%s#/oneOf/%d/properties/%s", typ.SchemaPath, variantIndex, name),
			Required:  required[name],
			Schema:    typ.SchemaPath,
			Stability: typ.Stability,
			Status:    "supported-generated",
			Type:      typ.TypeName,
		}, property)
		if err != nil {
			return nil, nil, fmt.Errorf("tagged union %s variant %d field %s: %w", typ.SchemaPath, variantIndex, name, err)
		}
		field = resolver.ResolveField(field)
		if !canGenerateFirstPassField(field, generatedNamedTypes) {
			return nil, nil, fmt.Errorf("tagged union %s variant %d field %s has unsupported generated kind %s", typ.SchemaPath, variantIndex, name, field.Kind)
		}
		fields = append(fields, field)
	}
	return fields, nullFields, nil
}

func canGenerateFirstPassType(typ TypePlan, enumTypes map[string]bool) bool {
	switch typ.Kind {
	case TypePlanEmptyStructCandidate:
		return true
	case TypePlanObjectStructCandidate:
		for _, field := range typ.Fields {
			if !canGenerateFirstPassField(field, enumTypes) {
				return false
			}
		}
		return true
	default:
		return false
	}
}

func canGenerateFirstPassField(field FieldPlan, generatedNamedTypes map[string]bool) bool {
	switch field.Kind {
	case FieldPlanAllOfRef, FieldPlanArrayRef, FieldPlanNullableRef, FieldPlanRef:
		return generatedNamedTypes[leafGoType(field.GoType)]
	case FieldPlanNullableScalar, FieldPlanNullableServiceTier:
		return firstPassGoTypeSafe(field.GoType)
	case FieldPlanArrayJSONValue,
		FieldPlanArrayScalar,
		FieldPlanArrayString,
		FieldPlanBool,
		FieldPlanJSONValue,
		FieldPlanJSONValueMap,
		FieldPlanOutputSchema,
		FieldPlanScalar,
		FieldPlanTypedMap:
		return firstPassGoTypeSafe(field.GoType) || generatedNamedTypes[leafGoType(field.GoType)]
	default:
		return false
	}
}

func isJSONRPCEnvelopeSchema(path string) bool {
	return strings.HasPrefix(path, "JSONRPC")
}

func firstPassGoTypeSafe(goType string) bool {
	leaf := leafGoType(goType)
	if inner, ok := nullableTypeArgument(leaf); ok {
		return firstPassGoTypeSafe(inner)
	}
	switch leaf {
	case "bool", "int32", "int64", "uint16", "uint32", "uint64", "string", "JSONValue", "OutputSchema", "Nullable[string]":
		return true
	default:
		return false
	}
}

func leafGoType(goType string) string {
	typ := generatedGoType(goType)
	for {
		if nullableInner, ok := nullableTypeArgument(typ); ok {
			typ = nullableInner
			continue
		}
		switch {
		case strings.HasPrefix(typ, "*"):
			typ = strings.TrimPrefix(typ, "*")
		case strings.HasPrefix(typ, "[]"):
			typ = strings.TrimPrefix(typ, "[]")
		case strings.HasPrefix(typ, "map[string]"):
			typ = strings.TrimPrefix(typ, "map[string]")
		default:
			return typ
		}
	}
}

func isDirectStringEnumSchema(schema *Schema) bool {
	if schema == nil || schema.Bool != nil {
		return false
	}
	if !schema.Type.Only("string") || len(schema.Enum) == 0 {
		return false
	}
	return len(unmodeledKeywords(schema)) == 0 &&
		schema.Ref == "" &&
		len(schema.Properties) == 0 &&
		len(schema.OneOf) == 0 &&
		len(schema.AnyOf) == 0 &&
		len(schema.AllOf) == 0 &&
		schema.Items == nil &&
		!schema.AdditionalProperties.Present
}

func stringEnumValues(schema *Schema) ([]string, bool) {
	if isDirectStringEnumSchema(schema) {
		return append([]string(nil), schema.Enum...), true
	}
	if values, ok := pureMultiOneOfStringEnumValues(schema); ok {
		return values, true
	}
	if schema == nil || schema.Bool != nil || !isPureSingleOneOfWrapper(schema) {
		return nil, false
	}
	variant := schema.OneOf[0]
	if !isDirectStringEnumSchema(variant) {
		return nil, false
	}
	return append([]string(nil), variant.Enum...), true
}

func reviewedStringEnumValues(schemaPath string, name string, schema *Schema) ([]string, bool) {
	if !isReviewedGeneratedDefinition(schemaPath, name) {
		return nil, false
	}
	return stringEnumValues(schema)
}

func pureMultiOneOfStringEnumValues(schema *Schema) ([]string, bool) {
	if schema == nil ||
		len(schema.OneOf) < 2 ||
		len(unmodeledKeywords(schema)) != 0 ||
		schema.Ref != "" ||
		len(schema.Type.Values) != 0 ||
		len(schema.Enum) != 0 ||
		len(schema.Required) != 0 ||
		len(schema.Properties) != 0 ||
		len(schema.AnyOf) != 0 ||
		len(schema.AllOf) != 0 ||
		len(schema.Definitions) != 0 ||
		schema.Items != nil ||
		schema.Default.Present ||
		schema.AdditionalProperties.Present {
		return nil, false
	}
	var values []string
	seen := map[string]bool{}
	for _, variant := range schema.OneOf {
		if !isDirectStringEnumSchema(variant) || len(variant.Enum) == 0 {
			return nil, false
		}
		for _, value := range variant.Enum {
			if seen[value] {
				return nil, false
			}
			seen[value] = true
			values = append(values, value)
		}
	}
	return values, true
}

func isPureSingleOneOfWrapper(schema *Schema) bool {
	return len(schema.OneOf) == 1 &&
		len(unmodeledKeywords(schema)) == 0 &&
		schema.Ref == "" &&
		len(schema.Type.Values) == 0 &&
		len(schema.Enum) == 0 &&
		len(schema.Required) == 0 &&
		len(schema.Properties) == 0 &&
		len(schema.AnyOf) == 0 &&
		len(schema.AllOf) == 0 &&
		len(schema.Definitions) == 0 &&
		schema.Items == nil &&
		!schema.Default.Present &&
		!schema.AdditionalProperties.Present
}

func sameStrings(left, right []string) bool {
	if len(left) != len(right) {
		return false
	}
	for index := range left {
		if left[index] != right[index] {
			return false
		}
	}
	return true
}

func writeEnumType(out *bytes.Buffer, enum EnumPlan) {
	fmt.Fprintf(out, "type %s string\n\n", enum.TypeName)
	out.WriteString("const (\n")
	for _, value := range enum.Values {
		constName := enumConstName(enum.TypeName, value)
		fmt.Fprintf(out, "\t%s %s = %s\n", constName, enum.TypeName, quoted(value))
	}
	out.WriteString(")\n\n")
	fmt.Fprintf(out, "func (value %s) IsValid() bool {\n", enum.TypeName)
	out.WriteString("\tswitch value {\n")
	for _, value := range enum.Values {
		fmt.Fprintf(out, "\tcase %s:\n", enumConstName(enum.TypeName, value))
		out.WriteString("\t\treturn true\n")
	}
	out.WriteString("\tdefault:\n")
	out.WriteString("\t\treturn false\n")
	out.WriteString("\t}\n")
	out.WriteString("}\n\n")
	fmt.Fprintf(out, "func (value %s) MarshalJSON() ([]byte, error) {\n", enum.TypeName)
	out.WriteString("\tif !value.IsValid() {\n")
	fmt.Fprintf(out, "\t\treturn nil, invalidEnumValue(%q, string(value))\n", enum.TypeName)
	out.WriteString("\t}\n")
	out.WriteString("\treturn json.Marshal(string(value))\n")
	out.WriteString("}\n\n")
	fmt.Fprintf(out, "func (value *%s) UnmarshalJSON(data []byte) error {\n", enum.TypeName)
	out.WriteString("\tvar raw string\n")
	out.WriteString("\tif err := json.Unmarshal(data, &raw); err != nil {\n")
	out.WriteString("\t\treturn err\n")
	out.WriteString("\t}\n")
	fmt.Fprintf(out, "\tparsed := %s(raw)\n", enum.TypeName)
	out.WriteString("\tif !parsed.IsValid() {\n")
	fmt.Fprintf(out, "\t\treturn invalidEnumValue(%q, raw)\n", enum.TypeName)
	out.WriteString("\t}\n")
	out.WriteString("\t*value = parsed\n")
	out.WriteString("\treturn nil\n")
	out.WriteString("}\n")
}

func writeScalarAliasType(out *bytes.Buffer, alias ScalarAliasPlan) {
	fmt.Fprintf(out, "type %s string\n", alias.TypeName)
}

func validateEnumConstNames(enum EnumPlan) error {
	seen := map[string]string{}
	for _, value := range enum.Values {
		constName := enumConstName(enum.TypeName, value)
		if previous, ok := seen[constName]; ok {
			return fmt.Errorf("generated enum %s has duplicate const name %s for values %q and %q", enum.TypeName, constName, previous, value)
		}
		seen[constName] = value
	}
	return nil
}

func enumConstName(typeName, value string) string {
	parts := identifierParts(value)
	if len(parts) == 0 {
		return typeName + "Unknown"
	}
	var b strings.Builder
	b.WriteString(typeName)
	for _, part := range parts {
		b.WriteString(goNamePart(part))
	}
	return b.String()
}

