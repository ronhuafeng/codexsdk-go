func writeTaggedUnionHelpers(out *bytes.Buffer) {
	out.WriteString("func decodeTaggedUnionDiscriminator(fields map[string]JSONValue, fieldName string, typeName string) (string, error) {\n")
	out.WriteString("\traw, ok := fields[fieldName]\n")
	out.WriteString("\tif !ok {\n")
	out.WriteString("\t\treturn \"\", missingRequiredField(typeName + \".\" + fieldName)\n")
	out.WriteString("\t}\n")
	out.WriteString("\tdelete(fields, fieldName)\n")
	out.WriteString("\tif raw.Kind() == JSONKindNull {\n")
	out.WriteString("\t\treturn \"\", fmt.Errorf(\"decode %s.%s: null is not allowed\", typeName, fieldName)\n")
	out.WriteString("\t}\n")
	out.WriteString("\tvalue, ok := raw.AsString()\n")
	out.WriteString("\tif !ok {\n")
	out.WriteString("\t\treturn \"\", fmt.Errorf(\"decode %s.%s: expected string\", typeName, fieldName)\n")
	out.WriteString("\t}\n")
	out.WriteString("\treturn value, nil\n")
	out.WriteString("}\n\n")
	out.WriteString("func invalidUnionValue(typeName string) error {\n")
	out.WriteString("\treturn fmt.Errorf(\"invalid %s union value: no variant is set\", typeName)\n")
	out.WriteString("}\n\n")
	out.WriteString("func invalidUnionVariant(typeName string, variant string) error {\n")
	out.WriteString("\treturn fmt.Errorf(\"invalid %s union value: variant %q has no payload\", typeName, variant)\n")
	out.WriteString("}\n\n")
	out.WriteString("func unknownUnionVariant(typeName string, discriminator string, variant string) error {\n")
	out.WriteString("\treturn fmt.Errorf(\"decode %s.%s: unknown variant %q\", typeName, discriminator, variant)\n")
	out.WriteString("}\n")
}

func writeTaggedUnionType(out *bytes.Buffer, union TaggedUnionPlan) {
	kindTypeName := union.TypeName + "Kind"
	fmt.Fprintf(out, "type %s string\n\n", kindTypeName)
	out.WriteString("const (\n")
	for _, variant := range union.Variants {
		fmt.Fprintf(out, "\t%s %s = %s\n", taggedUnionKindConstName(union, variant), kindTypeName, quoted(variant.DiscriminatorValue))
	}
	out.WriteString(")\n\n")

	fmt.Fprintf(out, "type %s struct {\n", union.TypeName)
	out.WriteString("\tkind " + kindTypeName + "\n")
	for _, variant := range union.Variants {
		fmt.Fprintf(out, "\t%s *%s\n", variant.PrivateFieldName, variant.PayloadTypeName)
	}
	out.WriteString("}\n\n")

	for _, variant := range union.Variants {
		writeTaggedUnionPayloadType(out, variant)
		out.WriteString("\n")
	}
	for _, variant := range union.Variants {
		writeTaggedUnionConstructor(out, union, variant)
		out.WriteString("\n")
	}
	writeTaggedUnionKindMethod(out, union)
	out.WriteString("\n")
	writeTaggedUnionIsValid(out, union)
	out.WriteString("\n")
	for _, variant := range union.Variants {
		writeTaggedUnionAccessor(out, union, variant)
		out.WriteString("\n")
	}
	writeTaggedUnionMarshal(out, union)
	out.WriteString("\n")
	writeTaggedUnionUnmarshal(out, union)
}

func writeTaggedUnionPayloadType(out *bytes.Buffer, variant TaggedUnionVariantPlan) {
	if len(variant.Fields) == 0 {
		fmt.Fprintf(out, "type %s struct{}\n", variant.PayloadTypeName)
		return
	}
	fmt.Fprintf(out, "type %s struct {\n", variant.PayloadTypeName)
	for _, field := range sortedFields(variant.Fields) {
		fmt.Fprintf(out, "\t%s %s `json:\"%s%s\"`\n",
			fieldGoName(field.FieldName),
			generatedGoType(field.GoType),
			field.FieldName,
			jsonTagOmitEmpty(field),
		)
	}
	out.WriteString("}\n")
}

func writeTaggedUnionConstructor(out *bytes.Buffer, union TaggedUnionPlan, variant TaggedUnionVariantPlan) {
	if len(variant.Fields) == 0 {
		fmt.Fprintf(out, "func %s() %s {\n", variant.ConstructorName, union.TypeName)
		fmt.Fprintf(out, "\tpayload := %s{}\n", variant.PayloadTypeName)
	} else {
		fmt.Fprintf(out, "func %s(payload %s) %s {\n", variant.ConstructorName, variant.PayloadTypeName, union.TypeName)
	}
	fmt.Fprintf(out, "\treturn %s{kind: %s, %s: &payload}\n", union.TypeName, taggedUnionKindConstName(union, variant), variant.PrivateFieldName)
	out.WriteString("}\n")
}

func writeTaggedUnionKindMethod(out *bytes.Buffer, union TaggedUnionPlan) {
	fmt.Fprintf(out, "func (value %s) Kind() %s {\n", union.TypeName, union.TypeName+"Kind")
	out.WriteString("\treturn value.kind\n")
	out.WriteString("}\n")
}

func writeTaggedUnionIsValid(out *bytes.Buffer, union TaggedUnionPlan) {
	fmt.Fprintf(out, "func (value %s) IsValid() bool {\n", union.TypeName)
	out.WriteString("\tswitch value.kind {\n")
	for _, variant := range union.Variants {
		fmt.Fprintf(out, "\tcase %s:\n", taggedUnionKindConstName(union, variant))
		fmt.Fprintf(out, "\t\treturn value.%s != nil\n", variant.PrivateFieldName)
	}
	out.WriteString("\tdefault:\n")
	out.WriteString("\t\treturn false\n")
	out.WriteString("\t}\n")
	out.WriteString("}\n")
}

func writeTaggedUnionAccessor(out *bytes.Buffer, union TaggedUnionPlan, variant TaggedUnionVariantPlan) {
	fmt.Fprintf(out, "func (value %s) %s() (%s, bool) {\n", union.TypeName, variant.AccessorName, variant.PayloadTypeName)
	fmt.Fprintf(out, "\tif value.kind != %s || value.%s == nil {\n", taggedUnionKindConstName(union, variant), variant.PrivateFieldName)
	fmt.Fprintf(out, "\t\treturn %s{}, false\n", variant.PayloadTypeName)
	out.WriteString("\t}\n")
	fmt.Fprintf(out, "\treturn *value.%s, true\n", variant.PrivateFieldName)
	out.WriteString("}\n")
}

func writeTaggedUnionMarshal(out *bytes.Buffer, union TaggedUnionPlan) {
	fmt.Fprintf(out, "func (value %s) MarshalJSON() ([]byte, error) {\n", union.TypeName)
	out.WriteString("\tswitch value.kind {\n")
	for _, variant := range union.Variants {
		fmt.Fprintf(out, "\tcase %s:\n", taggedUnionKindConstName(union, variant))
		fmt.Fprintf(out, "\t\tif value.%s == nil {\n", variant.PrivateFieldName)
		fmt.Fprintf(out, "\t\t\treturn nil, invalidUnionVariant(%q, %q)\n", union.TypeName, variant.DiscriminatorValue)
		out.WriteString("\t\t}\n")
		writeTaggedUnionVariantMarshal(out, union, variant)
	}
	out.WriteString("\tdefault:\n")
	fmt.Fprintf(out, "\t\treturn nil, invalidUnionValue(%q)\n", union.TypeName)
	out.WriteString("\t}\n")
	out.WriteString("}\n")
}

func writeTaggedUnionVariantMarshal(out *bytes.Buffer, union TaggedUnionPlan, variant TaggedUnionVariantPlan) {
	wireFields := append([]FieldPlan(nil), variant.Fields...)
	wireFields = append(wireFields, FieldPlan{
		FieldName: union.Discriminator,
		GoType:    "string",
		Required:  true,
	})
	wireFields = sortedFields(wireFields)
	for _, field := range sortedFields(variant.Fields) {
		fieldName := fieldGoName(field.FieldName)
		if fieldNeedsMarshalNilCheck(field) {
			fmt.Fprintf(out, "\t\tif value.%s.%s == nil {\n", variant.PrivateFieldName, fieldName)
			fmt.Fprintf(out, "\t\t\treturn nil, fmt.Errorf(\"encode %s.%s.%s: nil is not allowed\")\n", union.TypeName, variant.DiscriminatorValue, field.FieldName)
			out.WriteString("\t\t}\n")
		}
		if field.MinItems != nil {
			condition := minItemsViolationCondition("value."+variant.PrivateFieldName+"."+fieldName, field.GoType, minItemsLiteral(field))
			fmt.Fprintf(out, "\t\tif %s {\n", condition)
			fmt.Fprintf(out, "\t\t\treturn nil, fmt.Errorf(\"encode %s.%s.%s: must contain at least %s item\")\n", union.TypeName, variant.DiscriminatorValue, field.FieldName, minItemsLiteral(field))
			out.WriteString("\t\t}\n")
		}
		if field.Minimum != nil {
			condition := minimumViolationCondition("value."+variant.PrivateFieldName+"."+fieldName, field.GoType, minimumLiteral(field))
			fmt.Fprintf(out, "\t\tif %s {\n", condition)
			fmt.Fprintf(out, "\t\t\treturn nil, fmt.Errorf(\"encode %s.%s.%s: value must be >= %s\")\n", union.TypeName, variant.DiscriminatorValue, field.FieldName, minimumLiteral(field))
			out.WriteString("\t\t}\n")
		}
	}
	out.WriteString("\t\treturn json.Marshal(struct {\n")
	for _, field := range wireFields {
		fmt.Fprintf(out, "\t\t\t%s %s `json:\"%s%s\"`\n",
			fieldGoName(field.FieldName),
			generatedGoType(field.GoType),
			field.FieldName,
			jsonTagOmitEmpty(field),
		)
	}
	out.WriteString("\t\t}{\n")
	for _, field := range wireFields {
		fieldName := fieldGoName(field.FieldName)
		if field.FieldName == union.Discriminator {
			fmt.Fprintf(out, "\t\t\t%s: %s,\n", fieldName, quoted(variant.DiscriminatorValue))
			continue
		}
		fmt.Fprintf(out, "\t\t\t%s: value.%s.%s,\n", fieldName, variant.PrivateFieldName, fieldName)
	}
	out.WriteString("\t\t})\n")
}

func writeTaggedUnionUnmarshal(out *bytes.Buffer, union TaggedUnionPlan) {
	fmt.Fprintf(out, "func (value *%s) UnmarshalJSON(data []byte) error {\n", union.TypeName)
	fmt.Fprintf(out, "\tfields, err := decodeObjectFields(data, %q)\n", union.TypeName)
	out.WriteString("\tif err != nil {\n")
	out.WriteString("\t\treturn err\n")
	out.WriteString("\t}\n")
	fmt.Fprintf(out, "\tvariant, err := decodeTaggedUnionDiscriminator(fields, %q, %q)\n", union.Discriminator, union.TypeName)
	out.WriteString("\tif err != nil {\n")
	out.WriteString("\t\treturn err\n")
	out.WriteString("\t}\n")
	out.WriteString("\tswitch variant {\n")
	for _, variant := range union.Variants {
		fmt.Fprintf(out, "\tcase %s:\n", quoted(variant.DiscriminatorValue))
		fmt.Fprintf(out, "\t\tvar decoded %s\n", variant.PayloadTypeName)
		for _, field := range sortedFields(variant.Fields) {
			writeFieldDecoder(out, TypePlan{TypeName: union.TypeName}, field)
		}
		for _, field := range variant.NullFields {
			writeTaggedUnionNullFieldDecoder(out, union, variant, field)
		}
		fmt.Fprintf(out, "\t\tif err := rejectUnexpectedFields(fields, %q); err != nil {\n", union.TypeName+"."+variant.DiscriminatorValue)
		out.WriteString("\t\t\treturn err\n")
		out.WriteString("\t\t}\n")
		fmt.Fprintf(out, "\t\t*value = %s{kind: %s, %s: &decoded}\n", union.TypeName, taggedUnionKindConstName(union, variant), variant.PrivateFieldName)
		out.WriteString("\t\treturn nil\n")
	}
	out.WriteString("\tdefault:\n")
	fmt.Fprintf(out, "\t\treturn unknownUnionVariant(%q, %q, variant)\n", union.TypeName, union.Discriminator)
	out.WriteString("\t}\n")
	out.WriteString("}\n")
}

func writeTaggedUnionNullFieldDecoder(out *bytes.Buffer, union TaggedUnionPlan, variant TaggedUnionVariantPlan, field NullFieldPlan) {
	fieldPath := union.TypeName + "." + variant.DiscriminatorValue + "." + field.FieldName
	fmt.Fprintf(out, "\t\t%s, seen%s := fields[%q]\n", unexportedIdentifier(fieldGoName(field.FieldName)), fieldGoName(field.FieldName), field.FieldName)
	fmt.Fprintf(out, "\t\tif seen%s {\n", fieldGoName(field.FieldName))
	fmt.Fprintf(out, "\t\t\tdelete(fields, %q)\n", field.FieldName)
	fmt.Fprintf(out, "\t\t\tif %s.Kind() != JSONKindNull {\n", unexportedIdentifier(fieldGoName(field.FieldName)))
	fmt.Fprintf(out, "\t\t\t\treturn fmt.Errorf(\"decode %s: expected null\")\n", fieldPath)
	out.WriteString("\t\t\t}\n")
	out.WriteString("\t\t}\n")
	if field.Required {
		fmt.Fprintf(out, "\t\tif !seen%s {\n", fieldGoName(field.FieldName))
		fmt.Fprintf(out, "\t\t\treturn missingRequiredField(%q)\n", fieldPath)
		out.WriteString("\t\t}\n")
	}
}

func sortedFields(fields []FieldPlan) []FieldPlan {
	sorted := append([]FieldPlan(nil), fields...)
	sort.Slice(sorted, func(i, j int) bool {
		return sorted[i].FieldName < sorted[j].FieldName
	})
	return sorted
}

func writeDecodeHelpers(out *bytes.Buffer) {
	out.WriteString("func decodeObjectFields(data []byte, typeName string) (map[string]JSONValue, error) {\n")
	out.WriteString("\tvalue, err := ParseJSONValue(data)\n")
	out.WriteString("\tif err != nil {\n")
	out.WriteString("\t\treturn nil, fmt.Errorf(\"decode %s: %w\", typeName, err)\n")
	out.WriteString("\t}\n")
	out.WriteString("\tfields, ok := value.AsObject()\n")
	out.WriteString("\tif !ok {\n")
	out.WriteString("\t\treturn nil, fmt.Errorf(\"decode %s: expected object\", typeName)\n")
	out.WriteString("\t}\n")
	out.WriteString("\treturn fields, nil\n")
	out.WriteString("}\n\n")
	out.WriteString("func decodeJSONField[T any](fields map[string]JSONValue, fieldName string, fieldPath string, allowNull bool, target *T) (bool, error) {\n")
	out.WriteString("\traw, ok := fields[fieldName]\n")
	out.WriteString("\tif !ok {\n")
	out.WriteString("\t\treturn false, nil\n")
	out.WriteString("\t}\n")
	out.WriteString("\tdelete(fields, fieldName)\n")
	out.WriteString("\tif raw.Kind() == JSONKindNull && !allowNull {\n")
	out.WriteString("\t\treturn true, fmt.Errorf(\"decode %s: null is not allowed\", fieldPath)\n")
	out.WriteString("\t}\n")
	out.WriteString("\tdata, err := json.Marshal(raw)\n")
	out.WriteString("\tif err != nil {\n")
	out.WriteString("\t\treturn true, fmt.Errorf(\"decode %s: %w\", fieldPath, err)\n")
	out.WriteString("\t}\n")
	out.WriteString("\tif err := json.Unmarshal(data, target); err != nil {\n")
	out.WriteString("\t\treturn true, fmt.Errorf(\"decode %s: %w\", fieldPath, err)\n")
	out.WriteString("\t}\n")
	out.WriteString("\treturn true, nil\n")
	out.WriteString("}\n\n")
	out.WriteString("func decodeJSONValueField(fields map[string]JSONValue, fieldName string, fieldPath string, target *JSONValue) (bool, error) {\n")
	out.WriteString("\traw, ok := fields[fieldName]\n")
	out.WriteString("\tif !ok {\n")
	out.WriteString("\t\treturn false, nil\n")
	out.WriteString("\t}\n")
	out.WriteString("\tdelete(fields, fieldName)\n")
	out.WriteString("\t*target = raw.clone()\n")
	out.WriteString("\treturn true, nil\n")
	out.WriteString("}\n\n")
	out.WriteString("func decodeOptionalJSONValueField(fields map[string]JSONValue, fieldName string, fieldPath string, target **JSONValue) (bool, error) {\n")
	out.WriteString("\traw, ok := fields[fieldName]\n")
	out.WriteString("\tif !ok {\n")
	out.WriteString("\t\treturn false, nil\n")
	out.WriteString("\t}\n")
	out.WriteString("\tdelete(fields, fieldName)\n")
	out.WriteString("\tvalue := raw.clone()\n")
	out.WriteString("\t*target = &value\n")
	out.WriteString("\treturn true, nil\n")
	out.WriteString("}\n\n")
	out.WriteString("func decodeNullableJSONField[T any](fields map[string]JSONValue, fieldName string, fieldPath string, target **Nullable[T]) (bool, error) {\n")
	out.WriteString("\tvalue, seen, err := nullableJSONFieldValue[T](fields, fieldName, fieldPath)\n")
	out.WriteString("\tif err != nil || !seen {\n")
	out.WriteString("\t\treturn seen, err\n")
	out.WriteString("\t}\n")
	out.WriteString("\t*target = value\n")
	out.WriteString("\treturn true, nil\n")
	out.WriteString("}\n\n")
	out.WriteString("func decodeRequiredNullableJSONField[T any](fields map[string]JSONValue, fieldName string, fieldPath string, target *Nullable[T]) (bool, error) {\n")
	out.WriteString("\tvalue, seen, err := nullableJSONFieldValue[T](fields, fieldName, fieldPath)\n")
	out.WriteString("\tif err != nil || !seen {\n")
	out.WriteString("\t\treturn seen, err\n")
	out.WriteString("\t}\n")
	out.WriteString("\t*target = *value\n")
	out.WriteString("\treturn true, nil\n")
	out.WriteString("}\n\n")
	out.WriteString("func nullableJSONFieldValue[T any](fields map[string]JSONValue, fieldName string, fieldPath string) (*Nullable[T], bool, error) {\n")
	out.WriteString("\traw, ok := fields[fieldName]\n")
	out.WriteString("\tif !ok {\n")
	out.WriteString("\t\treturn nil, false, nil\n")
	out.WriteString("\t}\n")
	out.WriteString("\tdelete(fields, fieldName)\n")
	out.WriteString("\tdata, err := json.Marshal(raw)\n")
	out.WriteString("\tif err != nil {\n")
	out.WriteString("\t\treturn nil, true, fmt.Errorf(\"decode %s: %w\", fieldPath, err)\n")
	out.WriteString("\t}\n")
	out.WriteString("\tvalue, err := NullableFromJSON[T](data)\n")
	out.WriteString("\tif err != nil {\n")
	out.WriteString("\t\treturn nil, true, fmt.Errorf(\"decode %s: %w\", fieldPath, err)\n")
	out.WriteString("\t}\n")
	out.WriteString("\treturn value, true, nil\n")
	out.WriteString("}\n\n")
	out.WriteString("func missingRequiredField(fieldPath string) error {\n")
	out.WriteString("\treturn fmt.Errorf(\"decode %s: missing required field\", fieldPath)\n")
	out.WriteString("}\n\n")
	out.WriteString("func rejectUnexpectedFields(fields map[string]JSONValue, typeName string) error {\n")
	out.WriteString("\tif len(fields) == 0 {\n")
	out.WriteString("\t\treturn nil\n")
	out.WriteString("\t}\n")
	out.WriteString("\tnames := make([]string, 0, len(fields))\n")
	out.WriteString("\tfor name := range fields {\n")
	out.WriteString("\t\tnames = append(names, name)\n")
	out.WriteString("\t}\n")
	out.WriteString("\tsort.Strings(names)\n")
	out.WriteString("\treturn fmt.Errorf(\"decode %s: unknown field %q\", typeName, names[0])\n")
	out.WriteString("}\n\n")
	out.WriteString("func cloneJSONValueMap(values map[string]JSONValue) map[string]JSONValue {\n")
	out.WriteString("\tif len(values) == 0 {\n")
	out.WriteString("\t\treturn nil\n")
	out.WriteString("\t}\n")
	out.WriteString("\tcloned := make(map[string]JSONValue, len(values))\n")
	out.WriteString("\tfor key, value := range values {\n")
	out.WriteString("\t\tcloned[key] = value.clone()\n")
	out.WriteString("\t}\n")
	out.WriteString("\treturn cloned\n")
	out.WriteString("}\n")
}

func writeStructDecoder(out *bytes.Buffer, typ TypePlan, fields []FieldPlan) {
	fmt.Fprintf(out, "\nfunc (value *%s) UnmarshalJSON(data []byte) error {\n", typ.TypeName)
	fmt.Fprintf(out, "\tfields, err := decodeObjectFields(data, %q)\n", typ.TypeName)
	out.WriteString("\tif err != nil {\n")
	out.WriteString("\t\treturn err\n")
	out.WriteString("\t}\n")
	fmt.Fprintf(out, "\tvar decoded %s\n", typ.TypeName)
	for _, field := range fields {
		writeFieldDecoder(out, typ, field)
	}
	if typ.OpenDynamicProperties {
		out.WriteString("\tdecoded.DynamicProperties = cloneJSONValueMap(fields)\n")
	} else {
		fmt.Fprintf(out, "\tif err := rejectUnexpectedFields(fields, %q); err != nil {\n", typ.TypeName)
		out.WriteString("\t\treturn err\n")
		out.WriteString("\t}\n")
	}
	out.WriteString("\t*value = decoded\n")
	out.WriteString("\treturn nil\n")
	out.WriteString("}\n")
}

func writeFieldDecoder(out *bytes.Buffer, typ TypePlan, field FieldPlan) {
	fieldPath := typ.TypeName + "." + field.FieldName
	decodeFn := "decodeJSONField"
	typeArg := ""
	needsAllowNull := true
	switch {
	case isNullableGoType(field.GoType) && strings.HasPrefix(generatedGoType(field.GoType), "*"):
		decodeFn = "decodeNullableJSONField"
		typeArg = "[" + mustNullableTypeArgument(field.GoType) + "]"
		needsAllowNull = false
	case isNullableGoType(field.GoType):
		decodeFn = "decodeRequiredNullableJSONField"
		typeArg = "[" + mustNullableTypeArgument(field.GoType) + "]"
		needsAllowNull = false
	case field.Kind == FieldPlanJSONValue && field.Required:
		decodeFn = "decodeJSONValueField"
		needsAllowNull = false
	case field.Kind == FieldPlanJSONValue:
		decodeFn = "decodeOptionalJSONValueField"
		needsAllowNull = false
	case field.Kind == FieldPlanNullableServiceTier:
		decodeFn = "decodeNullableJSONField"
		typeArg = "[string]"
		needsAllowNull = false
	}
	seenName := "_"
	if field.Required {
		seenName = "seen" + fieldGoName(field.FieldName)
	}
	operator := ":="
	if !field.Required {
		operator = "="
	}
	fmt.Fprintf(out, "\t%s, err %s %s%s(fields, %q, %q, ",
		seenName,
		operator,
		decodeFn,
		typeArg,
		field.FieldName,
		fieldPath,
	)
	if needsAllowNull {
		fmt.Fprintf(out, "%t, ", field.WireAllowsNull)
	}
	fmt.Fprintf(out, "&decoded.%s)\n", fieldGoName(field.FieldName))
	out.WriteString("\tif err != nil {\n")
	out.WriteString("\t\treturn err\n")
	out.WriteString("\t}\n")
	if field.Required {
		fmt.Fprintf(out, "\tif !%s {\n", seenName)
		fmt.Fprintf(out, "\t\treturn missingRequiredField(%q)\n", fieldPath)
		out.WriteString("\t}\n")
	}
	if field.MinItems != nil {
		condition := minItemsViolationCondition("decoded."+fieldGoName(field.FieldName), field.GoType, minItemsLiteral(field))
		fmt.Fprintf(out, "\tif %s {\n", condition)
		fmt.Fprintf(out, "\t\treturn fmt.Errorf(\"decode %s: must contain at least %s item\")\n", fieldPath, minItemsLiteral(field))
		out.WriteString("\t}\n")
	}
	if field.Minimum != nil {
		condition := minimumViolationCondition("decoded."+fieldGoName(field.FieldName), field.GoType, minimumLiteral(field))
		fmt.Fprintf(out, "\tif %s {\n", condition)
		fmt.Fprintf(out, "\t\treturn fmt.Errorf(\"decode %s: value must be >= %s\")\n", fieldPath, minimumLiteral(field))
		out.WriteString("\t}\n")
	}
}

func isNullableGoType(goType string) bool {
	_, ok := nullableTypeArgument(generatedGoType(strings.TrimPrefix(goType, "*")))
	return ok
}

func mustNullableTypeArgument(goType string) string {
	inner, ok := nullableTypeArgument(generatedGoType(strings.TrimPrefix(goType, "*")))
	if !ok {
		panic(fmt.Sprintf("non-nullable Go type %q", goType))
	}
	return inner
}

func nullableTypeArgument(goType string) (string, bool) {
	const prefix = "Nullable["
	if !strings.HasPrefix(goType, prefix) || !strings.HasSuffix(goType, "]") {
		return "", false
	}
	return strings.TrimSuffix(strings.TrimPrefix(goType, prefix), "]"), true
}

func fieldGoName(name string) string {
	parts := identifierParts(name)
	if len(parts) == 0 {
		return "Field"
	}
	var b strings.Builder
	for _, part := range parts {
		b.WriteString(goNamePart(part))
	}
	return b.String()
}

func generatedGoType(goType string) string {
	return strings.ReplaceAll(goType, "protocolv2.", "")
}

func jsonTagOmitEmpty(field FieldPlan) string {
	if field.Required {
		return ""
	}
	return ",omitempty"
}

func reservedProtocolTypeName(name string) bool {
	switch name {
	case "JSONKind",
		"JSONValue",
		"MethodDirection",
		"MethodInfo",
		"MethodKind",
		"MethodStability",
		"Nullable",
		"OutputSchema",
		"ResponseSchemaStatus":
		return true
	default:
		return false
	}
}
